public class relational 
{
    public static void main(String[] args) 
    {
        int a = 20;
        int b = 50;
        if(a>b)
           System.out.println("a is greater");
        else if(a<b)
           System.out.println("a is smaller");
        else if(a==b)
           System.out.println("a is equal to b");
        else if(a!=b)
           System.out.println("a is not equal to b");
        
    }
    
}
