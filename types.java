public class types 
{
    public static void main(String[] args) 
    {   // integer
        byte a =127;
        short b = 555;
        int c = 50000;
        long d = 799755667;

        System.out.println(a + " " + b + " " + c + " " + d);

        //float
        float e = 5.6f;
        double f = 5.6;
        System.out.println(e + " " + f);

        //character
        char g = 'x';
        System.out.println(g);
        //boolean
        if(true)
           System.out.println("true");
        else
           System.out.println(false);
  
    }
    
}
